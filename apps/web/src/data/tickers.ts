// 合併所有清單（ETF 優先顯示）
export const ALL_TICKERS: TickerItem[] = [...POPULAR_ETFS, ...SP500_STOCKS];

// 建立快速查詢的 Set
const tickerSymbolSet = new Set(ALL_TICKERS.map((t) => t.symbol.toUpperCase()));

/**
 * 搜尋股票清單
 * @param query 搜尋關鍵字
 * @param limit 返回結果數量上限
 * @returns 匹配的股票清單
 */
export function searchTickers(query: string, limit: number = 10): TickerItem[] {
  if (!query || query.length === 0) {
    // 預設顯示熱門 ETF
    return POPULAR_ETFS.slice(0, limit);
  }

  const normalizedQuery = query.toUpperCase().trim();

  return ALL_TICKERS.filter(
    (item) =>
      item.symbol.toUpperCase().includes(normalizedQuery) ||
      item.name.toUpperCase().includes(normalizedQuery)
  )
    .sort((a, b) => {
      const aSymbol = a.symbol.toUpperCase();
      const bSymbol = b.symbol.toUpperCase();

      // 完全匹配優先
      if (aSymbol === normalizedQuery) return -1;
      if (bSymbol === normalizedQuery) return 1;

      // 開頭匹配次之
      const aStartsWith = aSymbol.startsWith(normalizedQuery);
      const bStartsWith = bSymbol.startsWith(normalizedQuery);
      if (aStartsWith && !bStartsWith) return -1;
      if (!aStartsWith && bStartsWith) return 1;

      // ETF 優先於股票
      if (a.category === "etf" && b.category !== "etf") return -1;
      if (a.category !== "etf" && b.category === "etf") return 1;

      // 按字母排序
      return aSymbol.localeCompare(bSymbol);
    })
    .slice(0, limit);
}

/**
 * 檢查股票代碼是否在靜態清單中
 * @param symbol 股票代碼
 * @returns 是否在清單中
 */
export function isInStaticList(symbol: string): boolean {
  return tickerSymbolSet.has(symbol.toUpperCase());
}

/**
 * 根據股票代碼獲取完整資訊
 * @param symbol 股票代碼
 * @returns 股票資訊或 undefined
 */
export function getTickerInfo(symbol: string): TickerItem | undefined {
  return ALL_TICKERS.find(
    (item) => item.symbol.toUpperCase() === symbol.toUpperCase()
  );
}
